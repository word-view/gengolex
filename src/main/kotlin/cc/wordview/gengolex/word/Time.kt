package cc.wordview.gengolex.word

/**
 * The time the verb indicates
 */
enum class Time {
    /**
     * Represent something that has happened in the past
     */
    PAST,

    /**
     * Represent something that is happening in the moment
     */
    PRESENT,

    /**
     * Represents something that will happen in the future
     */
    FUTURE,
}